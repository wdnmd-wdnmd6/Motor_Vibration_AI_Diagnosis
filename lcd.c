/* ---- Chinese 16x16 font rendering ---- */
#include "cn_font.h"

void LCD_DrawCNChar(uint16_t unicode, uint32_t x, uint32_t y, uint32_t color, uint32_t bg){
 const uint8_t* bm = cn_font_lookup(unicode);
 uint16_t f = rgb565(color), b = rgb565(bg);
 if(x+16>LCD_W || y+16>LCD_H) return;
 lcd_window(x, y, x+15, y+15);
 for(int r=0; r<16; r++){
  uint8_t lo = bm ? bm[r*2] : 0;
  uint8_t hi = bm ? bm[r*2+1] : 0;
  for(int c=0; c<8; c++){
   if(lo & (0x80>>c)) lcd_dat16(f); else lcd_dat16(b);
  }
  for(int c=0; c<8; c++){
   if(hi & (0x80>>c)) lcd_dat16(f); else lcd_dat16(b);
  }
 }
}

void LCD_DrawCNStr(const uint16_t* str, uint32_t x, uint32_t y, uint32_t color, uint32_t bg){
    while(*str){
        if(x+16>LCD_W){ x=0; y+=16; }
        if(y+16>LCD_H) return;
        LCD_DrawCNChar(*str++, x, y, color, bg);
        x+=16;
    }
}

/* 16x16 digit-based float display (digits 0-9, . and - must be in cn_font) */
void LCD_DrawFloat16(float f, int decimals, uint32_t x, uint32_t y, uint32_t color, uint32_t bg){
    if(f < 0){ LCD_DrawCNChar('-', x, y, color, bg); x += 16; f = -f; }
    int ip = (int)f;
    char buf[12]; int len = 0;
    if(ip == 0){ buf[len++] = '0'; }
    else { while(ip){ buf[len++] = '0' + (ip % 10); ip /= 10; } }
    for(int i = 0; i < len/2; i++){ char t = buf[i]; buf[i] = buf[len-1-i]; buf[len-1-i] = t; }
    for(int i = 0; i < len; i++){ LCD_DrawCNChar(buf[i], x, y, color, bg); x += 16; }
    if(decimals > 0){
        LCD_DrawCNChar('.', x, y, color, bg); x += 16;
        float fr = f - (float)(int)f;
        for(int i = 0; i < decimals; i++){
            fr *= 10.0f;
            int di = (int)fr;
            fr -= (float)di;
            LCD_DrawCNChar('0' + di, x, y, color, bg);
            x += 16;
        }
    }
}

/* ---- ST7789 init (exact sequence from working blue project) ---- */
static void st7789_init(void) {
 uart1_send("LCD: RST...\r\n");
 RST_LOW(); Delay_Ms(100);
 RST_HIGH(); Delay_Ms(200);

 lcd_cmd(0x11); Delay_Ms(120);  /* sleep out */
 lcd_cmd(0x36); lcd_dat(0x00);  /* MADCTL */
 lcd_cmd(0x3A); lcd_dat(0x05);  /* COLMOD: 16-bit RGB565 */

 lcd_cmd(0xB2); lcd_dat(0x0C); lcd_dat(0x0C);
 lcd_dat(0x00); lcd_dat(0x33); lcd_dat(0x33); /* PORCTRL */

 lcd_cmd(0xB7); lcd_dat(0x35);  /* GCTRL */
 lcd_cmd(0xBB); lcd_dat(0x32);  /* VCOMS */
 lcd_cmd(0xC2); lcd_dat(0x01);  /* VDVVRHEN */
 lcd_cmd(0xC3); lcd_dat(0x15);  /* VRHS */
 lcd_cmd(0xC4); lcd_dat(0x20);  /* VDVS */
 lcd_cmd(0xC6); lcd_dat(0x0F);  /* FRCTRL2 */
 lcd_cmd(0xD0); lcd_dat(0xA4); lcd_dat(0xA1); /* PWCTRL1 */

 lcd_cmd(0xE0);         /* PVGAMCTRL */
 lcd_dat(0xD0); lcd_dat(0x08); lcd_dat(0x0E); lcd_dat(0x09);
 lcd_dat(0x09); lcd_dat(0x05); lcd_dat(0x31); lcd_dat(0x33);
 lcd_dat(0x48); lcd_dat(0x17); lcd_dat(0x14); lcd_dat(0x15);
 lcd_dat(0x31); lcd_dat(0x34);

 lcd_cmd(0xE1);         /* NVGAMCTRL */
 lcd_dat(0xD0); lcd_dat(0x08); lcd_dat(0x0E); lcd_dat(0x09);
 lcd_dat(0x09); lcd_dat(0x15); lcd_dat(0x31); lcd_dat(0x33);
 lcd_dat(0x48); lcd_dat(0x17); lcd_dat(0x14); lcd_dat(0x15);
 lcd_dat(0x31); lcd_dat(0x34);

 lcd_cmd(0x21);         /* INVON */
 lcd_cmd(0x29);         /* DISPON */

 uart1_send("LCD: init complete\r\n");
}

void LCD_Init(void) {
    SPI2_Shared_Init();
    st7789_init();
}

/* HW reset after shared SPI bus noise */
void LCD_Reinit(void) {
    RST_LOW();  Delay_Ms(10);
    RST_HIGH(); Delay_Ms(150);
    lcd_cmd(0x11); Delay_Ms(120);  /* sleep out */
    lcd_cmd(0x36); lcd_dat(0x00);  /* MADCTL */
    lcd_cmd(0x3A); lcd_dat(0x05);  /* COLMOD: 16-bit */
    lcd_cmd(0x21);                  /* INVON */
    lcd_cmd(0x29); Delay_Ms(50);   /* DISPON */
}
