/*
 * File Name   : lcd.c
 * Description  : ST7789 240x240 SPI LCD driver
 *      CS=PB12 DC=PB10 RST=PB11
 *      SPI2 shared with IIS3DWB sensor
 */
#include "lcd.h"
#include "hardware.h"
#include "string.h"

#define CS_LOW() GPIO_ResetBits(GPIOB, GPIO_Pin_12)
#define CS_HIGH() GPIO_SetBits(GPIOB, GPIO_Pin_12)
#define DC_LOW() GPIO_ResetBits(GPIOB, GPIO_Pin_10)
#define DC_HIGH() GPIO_SetBits(GPIOB, GPIO_Pin_10)
#define RST_LOW() GPIO_ResetBits(GPIOB, GPIO_Pin_11)
#define RST_HIGH() GPIO_SetBits(GPIOB, GPIO_Pin_11)

/* ST7789 commands (DC before CS, matching blue project) */
static void lcd_cmd(uint8_t c) { DC_LOW(); __NOP(); CS_LOW(); SPI2_ReadWriteByte(c); __NOP(); CS_HIGH(); DC_HIGH(); __NOP(); }
static void lcd_dat(uint8_t d) { CS_LOW(); __NOP(); SPI2_ReadWriteByte(d); __NOP(); CS_HIGH(); __NOP(); }
static void lcd_dat16(uint16_t d) { CS_LOW(); __NOP(); SPI2_ReadWriteByte(d>>8); SPI2_ReadWriteByte(d&0xFF); __NOP(); CS_HIGH(); __NOP(); }

/* set window */
static void lcd_window(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2) {
 lcd_cmd(0x2A); lcd_dat16(x1); lcd_dat16(x2);
 lcd_cmd(0x2B); lcd_dat16(y1); lcd_dat16(y2);
 lcd_cmd(0x2C);
}

/* RGB888 -> RGB565 */
static uint16_t rgb565(uint32_t c) {
 uint8_t r=c&0xFF, g=(c>>8)&0xFF, b=(c>>16)&0xFF;
 return (uint16_t)(((r&0xF8)<<8)|((g&0xFC)<<3)|((b&0xF8)>>3));
}

/* ---- public API ---- */
void LCD_DrawRect(uint32_t color, uint32_t x, uint32_t y, uint32_t w, uint32_t h) {
 uint32_t ex=(x+w>LCD_W)?LCD_W:x+w, ey=(y+h>LCD_H)?LCD_H:y+h;
 uint16_t c=rgb565(color);
 lcd_window(x,y,ex-1,ey-1);
 uint32_t n=(ex-x)*(ey-y);
 while(n--) lcd_dat16(c);
}
void LCD_Clear(uint32_t c) { LCD_DrawRect(c,0,0,LCD_W,LCD_H); }

