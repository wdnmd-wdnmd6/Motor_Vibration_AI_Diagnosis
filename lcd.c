void LCD_PrintChar(char c, uint32_t x, uint32_t y, uint32_t co, uint32_t bg){
 if(c<0x20||c>0x7E)c=' ';
 const uint8_t *g=_font[c-0x20];
 uint16_t f=rgb565(co), b=rgb565(bg);
 lcd_window(x,y,x+5,y+7);
 for(int r=0;r<8;r++){
  for(int c2=0;c2<6;c2++){
   if(r<7&&c2<5&&(g[r]&(1<<(4-c2)))) lcd_dat16(f);
   else lcd_dat16(b);
  }
 }
}
